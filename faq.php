                    <p>For any material that is not a regular stock item or a stocked length or finish, there is a 1-2 week lead time. This can vary due to many factors, so <a>contact us</a> for any questions about your custom order.</p>
                    <br>
                    
                    <strong>What is the cost for a custom die to be made?</strong>
                    <p>We are limited to extrusions that fit within a 3 inch diameter circle. As long as they can, we can create any shape that you can dream of. Tooling for new dies varies depending on the complexity of the shape, cavities and wall thicknesses. Generally, solid dies start at $600 and hollow dies start at $1600. <a>Contact us</a> for any questions!</p>
                    <br>
                    
                    <strong>How much is the anodizing cost for mill finish material?</strong>
                    <p>Anodizing is done by an outside company. Generally, there is a minimum lot charge of $400 that is applied to every customly-anodized material. This does not apply to any of our material that is advertised as anodized or powder coated.</p>
                    <br>
                    
                    <strong>I need a cut length longer/shorter than listed, is this possible?</strong>
                    <p>We stock some of our material in 12 foot lengths--give us a call, <a>email</a> or <a>contact us</a> and ask us about any longer lengths. If material is not stocked in the length that you are looking for, we can always produce it in just about any length necessary. We also have several saws in-house and can cut down any of our material.</p>
                    <br>
                    
                    <strong>Is there a cutting charge?</strong>
                    <p>Cutting charges vary depending on the number of cuts and the precision/tolerance you seek. They range from $25-$100 for small orders. <a>Drop us a line</a> and we can create a custom quote.</p>
                    <br>
                    
                    <strong>What is the maximum length for ground shipping?</strong>
                    <p>All material under 10 feet (120 inches) can be shipped via FedEx Ground and Express. Any packages exactly 10 feet (120 inches) or longer must be shipped via Freight. We also have a will call option as well as same-day delivery for large packages in the Southern California area.</p>
                    <br>
                    
                    <strong>Same day shipping?</strong>
                    <p>If you live in our <a>local delivery zone</a> and we receive your order before 11 am Monday through Friday, we can have your package delivered the same day for $15 per box. Any orders received after 11 am will be delivered the next day. This applies to any package up to 12 feet long!.</p>
                    <br>
                    
